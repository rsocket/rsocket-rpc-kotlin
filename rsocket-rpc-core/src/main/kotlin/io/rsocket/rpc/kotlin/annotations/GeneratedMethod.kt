package io.rsocket.rpc.kotlin.annotations

import io.rsocket.rpc.kotlin.annotations.Annotation
import kotlin.reflect.KClass

@Annotation
@Target(AnnotationTarget.FUNCTION, AnnotationTarget.PROPERTY_GETTER, AnnotationTarget.PROPERTY_SETTER)
@Retention(AnnotationRetention.RUNTIME)
annotation class GeneratedMethod(
    /**
     * Type of the class returned from the generated method.
     *
     * @return parameterized type of return class
     */
    val returnTypeClass: KClass<*>
)
